/**
 * RTL + Indic + Amharic locale packs; also patches id kickoff typo.
 */
const fs = require("fs");
const path = require("path");

const OUT = path.join(__dirname, "i18n-source");
const en = JSON.parse(fs.readFileSync(path.join(OUT, "en.json"), "utf8"));
const KEYS = Object.keys(en);

function writeLocale(code, messages) {
  const missing = KEYS.filter((k) => !(k in messages));
  const extra = Object.keys(messages).filter((k) => !KEYS.includes(k));
  if (missing.length || extra.length) {
    throw new Error(
      `${code}: missing=[${missing}] extra=[${extra}] count=${Object.keys(messages).length}`,
    );
  }
  fs.writeFileSync(
    path.join(OUT, `${code}.json`),
    JSON.stringify(messages, null, 2) + "\n",
    "utf8",
  );
}

// Patch Indonesian kickoff after pack C wrote a typo
const idPath = path.join(OUT, "id.json");
if (fs.existsSync(idPath)) {
  const id = JSON.parse(fs.readFileSync(idPath, "utf8"));
  id.kickoff = "Kick-off";
  writeLocale("id", id);
}

